function addition(first, second){
    document.getElementById("res").value = Number(first) + Number(second);
}
function substract(first, second){
    document.getElementById("res").value = Number(first) - Number(second);
}
function multiply(first, second){
    document.getElementById("res").value = Number(first) * Number(second);
}
function division(first, second){
    document.getElementById("res").value = Number(first) / Number(second);
}